public class Rectangel { 
	float length,width;

	Rectangel(float length, float width) {
		super();
		this.length = length;
		this.width = width;
	}

	float getLength() {
		return length;
	}

	void setLength(float length) {
		this.length = length;
	}

	float getWidth() {
		return width;
	}

	void setWidth(float width) {
		this.width = width;
	} 
	

}
