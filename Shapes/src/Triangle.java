
public class Triangle extends Shape{ 
	float height,base;

	Triangle(float a, float b){  
		this.height=a; 
		this.base=b; 
		 
	}

	float getHeight() {
		return height;
	}

	void setHeight(float height) {
		this.height = height;
	}

	float getBase() {
		return base;
	}

	void setBase(float base) {
		this.base = base;
	}


	
} 
